# 练习:
#   输入两个整数,分别用变量 x, y 绑定
#     1. 计算这两个数的和并打印结果
#     2. 计算这两个数的积,并打印结果
#     3. 计算 x 的 y次方并打印
#   如:
#     请输入 x: 100
#     请输入 y: 200
#   打印如下:
#     100 + 200 = 300
#     100 * 200 = 20000
#     100 ** 200 = 1000000.....

s = input("请输入 x: ")  # 得到的是字符串
x = int(s)  # 转为整数
y = int(input("请输入 y: "))

print(x, '+', y, '=', x + y)
print(x, '*', y, '=', x * y)
print(x, '**', y, '=', x ** y)




