# 练习:
#   任意输入一个数字
#     1) 判断这个数是否大于100
#     2) 判断这个数是否小于0
#     3) 判断这个数是否在 50 ~ 150 之间

x = int(input('请输入一个整数: '))
# 1) 判断这个数是否大于100
if x > 100:
    print(x, "大于100")
else:
    print(x, "不大于100")

# 2) 判断这个数是否小于0
if x < 0:
    print(x, '小于0')
else:
    print(x, "不小于0")

# 3) 判断这个数是否在 50 ~ 150 之间
if 50 < x < 150:
    print(x, '在50~150之间')
else:
    print(x, '不在50~150之间')
