#   1. 写程序求下列算式的值:
#      1/1 - 1/3 + 1/5 - 1/7 + 1/9 -.... (+-)1/(2*n-1)
#      的和
#     求:
#       1) 当n等于10000时,此算式的和并打印
#       2) 将n等于10000时算式的和乘以4,打印其结果 (3.14)


s = 0  # 用于存储和
n = 1
sign = 1  # 代表当前的符号
while n <= 10000:
    s += sign * 1 / (2 * n - 1)
    n += 1
    sign *= -1 # 变换符中与

print("s=", s)
print('s*4=', s * 4)
