# range.py

# 此示例示意range函数的使用方法
for x in range(4):
    print(x)  # 0, 1, 2, 3

print("--------------")
for x in range(3, 6):
    print('x=', x)

print('==============')
for x in range(1, 10, 3):
    print("x =", x)  # 1, 4, 7

print('++++++++++++++')
for x in range(5, 0, -2):
    print(x)  # 5, 3, 1