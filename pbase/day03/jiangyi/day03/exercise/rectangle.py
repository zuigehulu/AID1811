
# 练习:
#   写一个程序，打印一个高度为4行的矩形方框
#   如:
#     请输入框形方框的宽度: 10
#   打印如下:
#     ##########
#     #        #
#     #        #
#     ##########
#   注:  宽度为输入数字的个数个#号


w = int(input("请输入框形方框的宽度: "))
print("#" * w)
print("#", ' ' * (w - 2), '#', sep='')
print("#", ' ' * (w - 2), '#', sep='')
print("#" * w)


