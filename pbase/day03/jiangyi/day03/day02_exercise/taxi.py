#   1. 北京出租车计价器
#     收费标准:
#       1. 3公里以内收费 13元
#       2. 基本单价 2.3 元/公里(超过3公里以外)
#       3. 空驶费: 超过15公里后,每公里加收单价的50%空驶费
#          (即3.45元/公里)
#     要求: 输入公里数,打印出费用金额(以元为单位精确到分)

km = int(input("请输入里程: "))
pay = 0  # 需要支付的钱数
if km <= 3:
    pay = 13
elif 3 < km <= 15:
    pay = 13 + 2.3 * (km - 3)
elif km > 15:
    pay = 13 + 2.3 * (km - 3) + 2.3 * .5 * (km - 15)

print("您需要支付:", round(pay, 2), '元')


