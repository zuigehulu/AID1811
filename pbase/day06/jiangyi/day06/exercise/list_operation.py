# 练习:
#   已知有列表:
#     L = [3, 5]
#     用索引和切片操作,将原列表改为:
#     L = [1, 2, 3, 4, 5, 6]
#     将列表反转,删除最后一个元素后再打印此列表
#     ...
#     print(L)  # [6, 5, 4, 3, 2]
#   (尽可能让L绑定的对象的ID不变)


L = [3, 5]
print(id(L))
L[1:1] = [4]
L[0:0] = [1, 2]
L[len(L):len(L)] = [6]
print(L)  # L = [1, 2, 3, 4, 5, 6]
L[:] = L[::-1]  # 用反转后的新列表的内容替换掉原列表的全部内容
L[-1:] = []  # 删除最后一个
#     将列表反转,删除最后一个元素后再打印此列表
#     ...
print(L)  # [6, 5, 4, 3, 2]
print(id(L))
#   (尽可能让L绑定的对象的ID不变)
