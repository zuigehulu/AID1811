#   2. 写一个程序,从键盘输入一段字符串,用变量s绑定
#      1) 将此字符串转为字节串用变量b绑定,并打印出此字节串b
#      2) 打印字符串s的长度和字节串b的长度
#      3) 将字节串b再转换为字符串用变量s2 绑定,然后判断
#        s2 和 s是否相同

s = input("请输入字符串: ")
b = s.encode('utf-8')
print('字符串长: ', len(s))
print('字节串长: ', len(b))

s2 = b.decode()
if s == s2:
    print("相同")
else:
    print("不同")