#   1.  有一只小猴子,摘了很多桃.
#      第一天吃了全部桃子的一半,感觉不饱又吃了一个
#      第二天吃了剩下的一半,感觉不饱又吃了一个
#      ... 以此类推
#      到第十天,发现只剩一个了
#     请问一天摘了多少桃子?

# day10 = 1
# day09 = (day10 + 1) * 2
# day08 = (day09 + 1) * 2

peach = 1  # 第十天的桃子数
for day in range(9, 0, -1):
    peach = (peach + 1) * 2  # 算出当天的桃子数
    print("第", day, '天有', peach, '个桃')

