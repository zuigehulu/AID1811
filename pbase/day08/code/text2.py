#  定义两个函数:
#      sum3(a, b, c)   用于返回三个数的和
#      pow3(x)         用于返回x的三次方
#     用以下函数计算:
#        1)  计算1的立方 + 2的立方 + 3的立方的和
#        2)  计算1+2+3的和的立方
#        即:1**3+2**3+3**3 和 (1+2+3) ** 3
def sum3(a,b,c):
    return a**3+b**3+c**3
def pow3(a,b,c):
    return(a+b+c)**3

print(sum3(1,2,3))
print(pow3(1,2,3))